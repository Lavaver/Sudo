package Console.ConsoleColor

/**# Console Foreground Color
 * 这是一个枚举类，用于存储并控制控制台输出颜色。
 * 众所周知，我这个人特别爱把 C# 的第一方包重写一遍（当然与文件和网络打交道的不会），然后为了给 Kotlin 版擦屁股就顺理成章的重写（什么 NTR）*/
enum class ConsoleColor {
    // The color black.
    BLACK,
    // The color dark blue.
    DARKBLUE,
    // The color dark green.
    DARKGREEN,
    // The color dark cyan (dark blue-green).
    DARKCYAN,
    // The color dark red.
    DARKRED,
    // The color dark magenta (dark purplish-red).
    DARKMAGENTA,
    // The color dark yellow (ochre).
    DARKYELLOW,
    // The color gray.
    GRAY,
    // The color dark gray.
    DARKGRAY,
    // The color blue.
    BLUE,
    // The color green.
    GREEN,
    // The color cyan (blue-green).
    CYAN,
    // The color red.
    RED,
    // The color magenta (purplish-red).
    MAGENTA,
    // The color yellow.
    YELLOW,
    // The color white.
    WHITE
}